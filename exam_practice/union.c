#include <unistd.h>

int	no_dup(int ac, char **av)
{
}

int	main(int ac, char **av)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (ac == 3)
	{
		while (av[1][i] != '\0')
		{
			while (av[2][j] != '\0')
			{
				if (no_dup(ac, av))
				{
					write(1, &av[1][i], 1);
					j++;
				}
				else
					j++;
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
